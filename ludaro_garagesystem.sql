CREATE TABLE ludaro_garagesystem (
    ID SMALLINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(99),
    coords VARCHAR(99),
    marker VARCHAR(99),
    blip VARCHAR(99),
    price INT,
    ipl VARCHAR(99),
    job VARCHAR(99),
    type VARCHAR(99),
    owner VARCHAR(99),
    npc VARCHAR(99),
    storage INT
);
